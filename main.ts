namespace pruebadua {
    //% block
    export function on(num1 : number, num2 : number) : number{
        let resultado = num1 + num2;
        return resultado;
    }
    //% block
    export function off(num1 : number, num2 : number) : number{
        let resultado = num1 - num2;
        return resultado;
    }
}
